import exemplos.ap as exp
import tc.ap as ap

def test_ap_pn2pf():
# (65) testando a função 'pn2pf' - Gustavo Pettine
  assert ap.pn2pf(exp.M1) == \
    (
      {'q1', 'p0', 'pf', 'q0'}, 
      {'0', '1'}, 
      {'0', '1', 'X', 'X0'}, 
      {
        ('p0', '0', 'X0'): ('q0', ['Z0', 'X0']), 
        ('p0', '1', 'X0'): ('q0', ['Z0', 'X0']), 
        ('q1', '', 'Z0'): [('pf', [])]
      },
      'p0', 
      'X0', 
      {'pf'}
    )

  assert ap.pn2pf(exp.M2) == \
    (
      {'q1', 'p0', 'pf', 'q0'}, 
      {'0', '', '1'}, 
      {'Z', 'X', 'X0'}, 
      {
        ('p0', '0', 'X0'): ('q0', ['Z', 'X0']), 
        ('p0', '', 'X0'): ('q0', ['Z', 'X0']), 
        ('p0', '1', 'X0'): ('q0', ['Z', 'X0']), 
        ('q0', '1', 'ZX0'): ('q0', ['X', 'Z'])
      }, 
      'p0', 
      'X0', 
      {'pf'}
    )

  assert ap.pn2pf(exp.M3) == \
    (
      {'q0', 'q1', 'q2', 'p0', 'pf'}, 
      {'1', '0'}, 
      {'1', 'X', '0', 'X0'},
      {
        ('p0', '1', 'X0'): ('q0', ['X', 'X0']), 
        ('q0', '1', 'XX0'): ('q1', ['X', 'X']), 
        ('p0', '0', 'X0'): ('q0', ['X', 'X0']), 
        ('q0', '0', 'XX0'): ('q0', []), 
        ('q1', '', 'X'): [('pf', [])]
      }, 
      'p0', 
      'X0', 
      {'pf'}
    )
