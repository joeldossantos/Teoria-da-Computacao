from tc import glc


def test_fng():
    assert glc.fng(({'S', '(', ')'},
                    {'(', ')', ''},
                    {('S', ('(', 'S', ')')), ('S', ('S', 'S')), ('S', (''))},
                    'S')) == (
           {')', '(', 'S'}, {'', '(', ')'}, {('S', ''), ('S', ('(', 'S', ')')), ('S', ('S', 'S'))}, 'S')

    assert glc.fng(({'S'},
                    {1, '+', 'a'},
                    {('S', ('S', '+', 'S')), ('S', (1)), ('S', ('a'))},
                    'S')) == ({'S'}, {'+', 1, 'a'}, {('S', ('S', '+', 'S')), ('S', 1), ('S', 'a')}, 'S')

    assert glc.fng(({'S', 'Q'}, {'(', '+', '-'}, {('S', ('(', 'S')), ('S', ('Q')),
                                                  ('Q', ('+', 'Q')), ('Q', ('-', 'Q')), ('Q', ())}, 'S')) == (
               {'Q', 'S'}, {'+', '(', '-'},
               {('Q', ()), ('Q', ('-', 'Q')), ('S', ('(', 'S')), ('Q', ('+', 'Q')), ('S', 'Q')}, 'S')
